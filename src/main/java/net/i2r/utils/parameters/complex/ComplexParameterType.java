package net.i2r.utils.parameters.complex;

/**
 * Define types of the complex parameters.
 */
public enum ComplexParameterType {
	/**
	 * List of parameters type.
	 */
	LIST,

	/**
	 * Set of parameters type.
	 */
	SET,

	/**
	 * Map of parameters type.
	 */
	MAP,

	/**
	 * Complex object type.
	 */
	OBJECT,

	/**
	 * Custom type.
	 */
	CUSTOM;

}
